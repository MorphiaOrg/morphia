package dev.morphia.critter.parser.generator;

import java.lang.annotation.Annotation;
import java.lang.constant.ClassDesc;
import java.lang.constant.ConstantDescs;
import java.lang.constant.MethodTypeDesc;
import java.lang.reflect.Modifier;
import java.util.LinkedHashSet;
import java.util.List;
import java.util.Set;

import dev.morphia.annotations.Entity;
import dev.morphia.annotations.ExternalEntity;
import dev.morphia.critter.CritterClassLoader;
import dev.morphia.mapping.Mapper;
import dev.morphia.mapping.codec.pojo.PropertyModel;
import dev.morphia.mapping.codec.pojo.critter.CritterEntityModel;

import io.github.dmlloyd.classfile.ClassFile;

/**
 * Generates a ClassFile-based {@code CritterEntityModel} implementation for a Morphia entity class.
 */
public class EntityModelGenerator extends BaseGenerator {
    private final Mapper mapper;
    private final List<PropertyModelGenerator> properties;
    private final Entity entityAnnotation;
    private final ExternalEntity externalEntity;
    private final List<Annotation> morphiaAnnotations;

    /**
     * Creates a new entity model generator for the given entity, reading entity-level annotations
     * from {@code standinType} (which differs from {@code type} only for {@code @ExternalEntity}).
     *
     * @param mapper             the Morphia mapper
     * @param type               the target class for which code is generated (instances of this type are persisted)
     * @param standinType        the class carrying the entity-level Morphia annotations; equals {@code type} for normal entities
     * @param critterClassLoader the class loader that will receive the generated bytecode
     * @param properties         the property model generators for each of the entity's properties
     * @throws IllegalStateException if neither {@code @Entity} nor {@code @ExternalEntity} is present on {@code standinType}
     */
    public EntityModelGenerator(Mapper mapper, Class<?> type, Class<?> standinType, CritterClassLoader critterClassLoader,
            List<PropertyModelGenerator> properties) {
        super(type, critterClassLoader);
        this.mapper = mapper;
        this.properties = properties;
        generatedType = "%s.%sEntityModel".formatted(baseName, type.getSimpleName());

        this.entityAnnotation = standinType.getAnnotation(Entity.class);
        this.externalEntity = standinType.getAnnotation(ExternalEntity.class);
        if (this.entityAnnotation == null && this.externalEntity == null) {
            throw new IllegalStateException(
                    "Class %s does not have @Entity or @ExternalEntity annotation".formatted(standinType.getName()));
        }

        // Collect morphia annotations from the stand-in class and its ancestors (de-duplicated).
        // For @ExternalEntity, these are the annotations the user placed on the stand-in's fields.
        Set<String> registered = new LinkedHashSet<>();
        List<Annotation> allAnnotations = new java.util.ArrayList<>();
        Class<?> current = standinType;
        while (current != null && current != Object.class) {
            for (Annotation a : current.getAnnotations()) {
                if (a.annotationType().getName().startsWith("dev.morphia.annotations.")
                        && registered.add(a.annotationType().getName())) {
                    allAnnotations.add(a);
                }
            }
            current = current.getSuperclass();
        }
        this.morphiaAnnotations = List.copyOf(allAnnotations);
    }

    /**
     * Creates a new entity model generator where the entity class is also the annotation source.
     */
    public EntityModelGenerator(Mapper mapper, Class<?> type, CritterClassLoader critterClassLoader,
            List<PropertyModelGenerator> properties) {
        this(mapper, type, type, critterClassLoader, properties);
    }

    /**
     * Returns the annotation of the given type from the entity class, or {@code null} if not present.
     */
    @SuppressWarnings("unchecked")
    public <T extends Annotation> T annotation(Class<T> type) {
        return entity.getAnnotation(type);
    }

    /**
     * Returns the fully-qualified name of the generated entity model class.
     */
    public String getGeneratedType() {
        return generatedType;
    }

    /**
     * Emits the generated entity model class and returns this generator.
     */
    public EntityModelGenerator emit() {
        ClassDesc thisDesc = ClassDesc.of(generatedType);
        ClassDesc superDesc = ClassDesc.of(CritterEntityModel.class.getName());
        ClassDesc mapperDesc = ClassDesc.of(Mapper.class.getName());
        ClassDesc entityModelDesc = ClassDesc.of(dev.morphia.mapping.codec.pojo.EntityModel.class.getName());
        ClassDesc propertyModelDesc = ClassDesc.of(PropertyModel.class.getName());
        ClassDesc annotationDesc = ClassDesc.of(Annotation.class.getName());
        String collectionNameStr = computeCollectionName();
        String discriminatorStr = computeDiscriminator();
        String discriminatorKeyStr = computeDiscriminatorKey();
        boolean isAbstractFlag = Modifier.isAbstract(entity.getModifiers());
        boolean isInterfaceFlag = entity.isInterface();
        boolean useDiscriminatorFlag = entityAnnotation != null ? entityAnnotation.useDiscriminator()
                : externalEntity.useDiscriminator();

        byte[] bytes = ClassFile.of().build(thisDesc, cb -> {
            cb.withVersion(ClassFile.JAVA_17_VERSION, 0);
            cb.withFlags(ClassFile.ACC_PUBLIC | ClassFile.ACC_SUPER);
            cb.withSuperclass(superDesc);

            // Constructor: (Mapper) -> void
            cb.withMethodBody("<init>", MethodTypeDesc.of(ConstantDescs.CD_void, mapperDesc),
                    ClassFile.ACC_PUBLIC, cod -> {
                        cod.aload(0);
                        cod.aload(1);
                        GenerationUtils.emitClassRef(cod, entity);
                        cod.invokespecial(superDesc, "<init>",
                                MethodTypeDesc.of(ConstantDescs.CD_void, mapperDesc, ConstantDescs.CD_Class));

                        // setType(entityClass)
                        cod.aload(0);
                        GenerationUtils.emitClassRef(cod, entity);
                        cod.invokevirtual(thisDesc, "setType",
                                MethodTypeDesc.of(ConstantDescs.CD_void, ConstantDescs.CD_Class));

                        // Add property models
                        for (PropertyModelGenerator prop : properties) {
                            ClassDesc propDesc = ClassDesc.of(prop.generatedType);
                            cod.aload(0);
                            cod.new_(propDesc);
                            cod.dup();
                            cod.aload(0);
                            cod.invokespecial(propDesc, "<init>",
                                    MethodTypeDesc.of(ConstantDescs.CD_void, entityModelDesc));
                            cod.invokevirtual(entityModelDesc, "addProperty",
                                    MethodTypeDesc.of(ConstantDescs.CD_boolean, propertyModelDesc));
                            cod.pop();
                        }

                        // Morphia annotations via builders — no reflection
                        for (Annotation ann : morphiaAnnotations) {
                            cod.aload(0);
                            GenerationUtils.emitAnnotationViaBuilder(cod, ann);
                            cod.invokevirtual(entityModelDesc, "annotation",
                                    MethodTypeDesc.of(ConstantDescs.CD_void, annotationDesc));
                        }

                        // configureProperties(mapper)
                        cod.aload(0);
                        cod.aload(1);
                        cod.invokevirtual(thisDesc, "configureProperties",
                                MethodTypeDesc.of(ConstantDescs.CD_void, mapperDesc));

                        cod.return_();
                    });

            // collectionName(): String
            cb.withMethodBody("collectionName", MethodTypeDesc.of(ConstantDescs.CD_String),
                    ClassFile.ACC_PUBLIC, cod -> {
                        cod.ldc(collectionNameStr);
                        cod.areturn();
                    });

            // discriminator(): String
            cb.withMethodBody("discriminator", MethodTypeDesc.of(ConstantDescs.CD_String),
                    ClassFile.ACC_PUBLIC, cod -> {
                        cod.ldc(discriminatorStr);
                        cod.areturn();
                    });

            // discriminatorKey(): String
            cb.withMethodBody("discriminatorKey", MethodTypeDesc.of(ConstantDescs.CD_String),
                    ClassFile.ACC_PUBLIC, cod -> {
                        cod.ldc(discriminatorKeyStr);
                        cod.areturn();
                    });

            // isAbstract(): boolean
            GenerationUtils.emitBooleanMethod(cb, "isAbstract", isAbstractFlag);
            // isInterface(): boolean
            GenerationUtils.emitBooleanMethod(cb, "isInterface", isInterfaceFlag);
            // useDiscriminator(): boolean
            GenerationUtils.emitBooleanMethod(cb, "useDiscriminator", useDiscriminatorFlag);
        });

        critterClassLoader.register(generatedType, bytes);
        return this;
    }

    private String computeCollectionName() {
        String key = entityAnnotation != null ? entityAnnotation.value() : externalEntity.value();
        return Mapper.IGNORED_FIELDNAME.equals(key)
                ? mapper.getConfig().collectionNaming().apply(entity.getSimpleName())
                : key;
    }

    private String computeDiscriminator() {
        String disc = entityAnnotation != null ? entityAnnotation.discriminator() : externalEntity.discriminator();
        return mapper.getConfig().discriminator().apply(entity, disc);
    }

    private String computeDiscriminatorKey() {
        String key = entityAnnotation != null ? entityAnnotation.discriminatorKey() : externalEntity.discriminatorKey();
        return Mapper.IGNORED_FIELDNAME.equals(key)
                ? mapper.getConfig().discriminatorKey()
                : key;
    }

}
